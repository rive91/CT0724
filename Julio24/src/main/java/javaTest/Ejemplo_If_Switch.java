package javaTest;


public class Ejemplo_If_Switch {

	public static void main(String[] args) {
		int i = 1;
		
		switch (i) {
		case 0:
			System.out.println("i es 0");
			break;
		case 1:
			System.out.println("i es 1");
			break;
		case 2:
			System.out.println("i es 2");
			break;
		case 3:
			System.out.println("i es 3");
			break;
		default:
			System.out.println("i es mayor que 3");
			break;
		}
	}

}
